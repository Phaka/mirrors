#!/bin/sh
set -euo pipefail
shopt -u dotglob
find * -prune -type d | while IFS= read -r d; do 
    echo " "
    echo "----------------------------------------------------------------------------"
    echo "--- $d"
    echo "----------------------------------------------------------------------------"
    pushd "$d" > /dev/null
    bash $d/mirror.sh
    popd > /dev/null
done
echo " "
echo " "
echo "----------------------------------------------------------------------------"
echo "--- Done"
echo "----------------------------------------------------------------------------"